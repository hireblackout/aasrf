        # Count vulnerabilities by severity
        critical = sum(1 for v in results["vulnerabilities"] if v.get("severity") == "CRITICAL" and v.get("vulnerable"))
        high = sum(1 for v in results["vulnerabilities"] if v.get("severity") == "HIGH" and v.get("vulnerable"))
        medium = sum(1 for v in results["vulnerabilities"] if v.get("severity") == "MEDIUM" and v.get("vulnerable"))
        low = sum(1 for v in results["vulnerabilities"] if v.get("severity") == "LOW" and v.get("vulnerable"))
        
        # Generate vulnerability HTML
        vuln_html = ""
        for vuln in results["vulnerabilities"]:
            status = "VULNERABLE âœ—" if vuln.get("vulnerable") else "SAFE âœ“"
            status_class = "vulnerable" if vuln.get("vulnerable") else "safe"
            
            severity = vuln.get("severity", "UNKNOWN")
            badge_class = f"badge-{severity.lower()}"
            
            evidence_html = ""
            if vuln.get("evidence"):
                evidence_html = f'<div class="evidence">{vuln["evidence"][:200]}...</div>'
            
            vuln_html += f'''
            <div class="vulnerability">
                <h3>
                    {vuln.get("test_name", "Unknown Test")}
                    <span class="severity-badge {badge_class}">{severity}</span>
                </h3>
                <p><strong>Status:</strong> <span class="{status_class}">{status}</span></p>
                <p><strong>CVE/Reference:</strong> {vuln.get("cve", "N/A")}</p>
                <p><strong>Description:</strong> {vuln.get("description", "No description")}</p>
                {evidence_html}
            </div>
            '''
        
